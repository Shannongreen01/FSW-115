async function getData(){
    
    try {
    const response = await axios.get("https://rickandmortyapi.com/api/character/3") 
    const origin = await axios.get(response.data.origin.url)
    const location = await axios.get(response.data.location.url)
    listToDom(origin, location, response)
    }

    catch(error) {
    console.log(error)
    }
}
