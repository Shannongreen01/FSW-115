    const live = document.createElement("h1")
    live.textContent = "Status: " + cname.data.status
    mainInfo.appendChild(live)
    const h1 = document.createElement("h1")
    h1.textContent = "Origin: " + orgin.data.name
    mainInfo.appendChild(h1)
